function Toggle(){
    let menu = document.getElementById("show");

    if(menu.style.display === 'block'){
        menu.style.display === 'none'
        
    }else{
        menu.style.display = 'block';

    }
}

function hideMenu(){
    let menu = document.getElementById("show");
    if(menu.style.display === 'block'){
         menu.style.display === 'none'
        
    }else{
        menu.style.display = 'block';
    }
}